# imprimir strings no console
print('Olá Mundo!')

# imprimir strings com aspas duplas
print("Olá Mundo!!")

# operação aritmética
print(2 + 3)

# string textual
print('2 + 3')

# concatenação de textos
print('2' + '3')

# concatenando mensagens
print('Olá, ' + 'mundo')
print('Olá, ', 'Mundo!')

# concatenando mensagens e números
print('O resultado da soma de 2 + 3 é ', 2 + 3)

# operadores matemáticos
print(10 * (5 + 7) / 4)

# operadores matemáticos (sem prioridade)
print(10 * (5 + 7 / 4))